#include <iostream>

int main() {
    // 1. Apakah keluaran dari potongan program berikut?
    std::cout << "9 kuadrat adalah ";
    std::cout << "9 * 9" << std::endl;
    // 9 kuadrat adalah 9 * 9

    // 2. Di antara potongan-potongan program di bawah ini, mana sajakah potongan program yang menghasilkan keluaran 42? Pilih semua yang memenuhi.
    std::cout << 42 << std::endl;
    std::cout << "42" << std::endl;
    std::cout << 40 + 11 / (5 - 1) << std::endl;
    // semua benar
}
